// 1) 고단가(수익형) 키워드 추출 프롬프트
function keywordPrompt(topic) {
  return `너는 네이버 블로그 SEO와 수익화에 특화된 마케터야.
내 목표는 "조회수"가 아니라 "수익"이 나는 키워드를 찾는 거야.
아래 조건을 모두 반영해서 키워드를 표로 정리해줘.

[주제]
: ${topic}

[조건]
1. 광고주가 실제로 돈을 쓰는 키워드
2. 구매 의도가 높은 키워드 (정보 탐색 X, 행동 직전)
3. 네이버 블로그에서 상위노출 가능한 롱테일 키워드
4. 체험단 / 협찬 / 제휴 / 예약 / 상담으로 연결 가능한 키워드
5. 초보 블로그도 공략 가능한 경쟁도 중~하 키워드

[출력 형식]
아래 4가지 카테고리로 나눠서 총 20개 이상 제시
1) 고단가 서비스형 키워드
2) 구매 직전 키워드 (가격/후기/비교/추천 포함)
3) 지역 + 업종 키워드
4) 협찬/체험단 연결 키워드
각 키워드는 [키워드 / 검색의도 / 예상 수익화 방식 / 난이도 / 돈 되는 이유] 형식의 표로.

[추가 요청]
- "조회수는 높지만 돈 안 되는 키워드" 예시 5개
- 바로 글 써도 되는 BEST 5도 따로 뽑아줘 (★표시)`;
}

// 2) 네이버 좋은 콘텐츠 5원칙 기반 SEO 본문 → 발행용 JSON
function articleJsonPrompt(topic, keywordResult) {
  return `너는 네이버 블로그 SEO·수익화 전문 작가이자, 네이버 검색이 공식 안내한 "좋은 콘텐츠 5원칙"을 철저히 지키는 에디터야.
말투는 친근하고 신뢰감 있게, 광고 티 안 나게. 단, 지어낸 개인 경험담·허위 후기·근거 없는 수치는 절대 금지(네이버가 저품질로 지목함).

[배경]
- 주제: ${topic}
- 아래는 방금 뽑은 수익형 키워드 분석. 여기서 BEST 5 중 가장 유망한 메인 키워드 1개를 골라 글을 써.

[키워드 분석 결과]
${keywordResult}

[네이버 좋은 콘텐츠 5원칙 — 반드시 반영]
1) 독자·목적(TPO): 서론에서 "누가, 언제, 어떤 상황에서, 어떤 목적"으로 이 글을 찾는지 구체적으로 규정하고 그 독자에 맞춰 써.
2) 구체적 절차: 해결 과정을 시간/단계 순서로 구체적으로. 흔히 놓치는 팁·주의사항 포함.
3) 대안 비교: 선택지 2~3개를 항목별 장단점으로 비교하고 "이런 상황엔 A, 저런 상황엔 B"처럼 상황별 추천 제시(가능하면 간단한 비교 표 형태의 텍스트).
4) 객관적 근거: 개인 경험을 지어내지 말고 공개된 사실·공식 정보·일반적으로 알려진 기준·수치 범위로 뒷받침. 불확실한 수치는 "대략/보통" 수준으로만.
5) 구조화 + 맥락 이미지: 소제목·리스트로 빠르게 파악되게 구조화. 이미지 자리는 본문 내용과 직접 관련된 곳에.

[작성 규칙]
- 클릭되는 제목 8개를 머릿속으로 만들고 검색노출+클릭률 최고 1개 선택(숫자/궁금증/혜택).
- 하나의 일관된 주제로, 1500자 이상, 소제목 3~5개.
- 서론(독자 고민+TPO) -> 본론(절차/비교/근거) -> 결론(상황별 추천+행동유도).
- 메인 키워드를 제목·서론·소제목·본문에 자연스럽게 반복, 롱테일도 녹임.
- 본문 흐름에 맞는 이미지 자리 2~3곳에 [IMAGE_HERE] 한 줄.
- 태그 7~10개. 최신성: 2026년 시점 언급이 자연스러우면 반영.
- 가짜 1인칭 경험·허위 후기·허위 수치 금지.

[출력 형식 — 순수 JSON 하나만, 코드블록/설명 없이]
{
  "title": "최종 제목",
  "main_keyword": "선택한 메인 키워드",
  "content": "소제목·문단 포함 본문. 이미지 자리는 [IMAGE_HERE] 한 줄. 줄바꿈은 \\n 사용.",
  "tags": ["태그1","태그2","..."],
  "image_queries": ["scene one","scene two","..."]
}
image_queries는 [IMAGE_HERE] 개수·순서를 정확히 맞춰서, i번째 검색어는 i번째 [IMAGE_HERE] 바로 앞 내용을 시각화한 것. 각 검색어는 스톡사진이 잘 잡히는 "구체적 장면 영어 3~5단어"(예: "person working laptop office desk"). 추상어·문장부호·한국어·브랜드명·로고 금지.`;
}


// 3) 블로그 글 -> 틱톡/인스타 카드뉴스(세로 9:16) 대본
function cardNewsPrompt(title, content) {
  return `너는 숏폼 카드뉴스 전문 편집자야. 아래 블로그 글을 틱톡/인스타용 세로 카드뉴스로 각색해줘.

[원문 제목]
${title}

[원문 본문]
${content}

[규칙]
- 총 7~9장. 1장은 스크롤 멈추게 하는 강력한 "표지(후킹)" — 궁금증/숫자/혜택.
- 마지막 1장은 CTA(팔로우·저장·댓글 유도).
- 중간 카드들은 원문의 핵심을 순서대로. 한 카드에 한 메시지만.
- 카드당 title은 3~14자의 굵은 핵심 문구, body는 1~3줄(각 줄 22자 이내)로 짧게.
- 모바일에서 한눈에 읽히게 쉬운 말로. 필요시 이모지 1개 정도.
- 광고 티/허위 정보 금지.

[출력 형식 — 순수 JSON 하나만, 코드블록/설명 없이]
{
  "title": "카드뉴스 전체 제목",
  "cards": [
    { "kind": "cover",  "title": "표지 후킹 문구", "body": "부제 한 줄" },
    { "kind": "body",   "title": "핵심 소제목",   "body": "설명 1~3줄" },
    { "kind": "cta",    "title": "행동 유도",      "body": "팔로우/저장 유도 한두 줄" }
  ]
}`;
}


// 4) 카드뉴스/영상용 틱톡 캡션 + 해시태그
function tiktokCaptionPrompt(title, content) {
  return `아래 콘텐츠를 틱톡/릴스용 캡션으로 만들어줘.

[제목] ${title}
[내용 요약 재료] ${content}

[규칙]
- 첫 줄은 스크롤 멈추는 후킹 한 줄(궁금증/공감).
- 2~3줄 짧은 설명. 이모지 1~3개.
- 마지막 줄에 해시태그 8~12개(한국어+관련 키워드). 브랜드/허위 금지.
[출력 형식 — 순수 JSON 하나만]
{ "caption": "후킹\n설명\n#해시 #태그 ..." }`;
}

module.exports = { keywordPrompt, articleJsonPrompt, cardNewsPrompt, tiktokCaptionPrompt };
